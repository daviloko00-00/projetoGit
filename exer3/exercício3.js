let numexpresso = parseInt(prompt("Digite um número: "));// Lê um número inteiro do usuário
// Verifica se o número é par, ímpar ou zero
if (numexpresso % 2 == 0) {
    alert("O número é par.");
}
if (numexpresso % 2 != 0) {
    alert("O número é ímpar.");
}
if (numexpresso == 0) {
    alert("O número é zero.");
}